package com.example.bolsa;

public class Nombres {
	protected int id;
	protected String name;

	// Constructor
	public Nombres(int id, String name) {
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
